<h3>Welcome to the ultimate weight loss calculator!</h3>

There are tons of BMI calculators, BMR calculators, calorie deficit calculators, TDEE calculators, ideal weight calculators, macro calculators, and weight loss predictors on the web – but I think this is the first time they have been brought together and joined into one huge awesome ultimate weightloss calculator! I hope that this will be useful for anyone who is looking for information to help them control their weight and plan their diets. The weight loss predictor is my favourite part - great for motivation! Right now we have:

 

<h4>Basic Calculations:</h4>

<ul>
<li>Ideal weight range for your height, adjusted +10% or -10% depending on frame size. (This is based on the BMI calculation, and provides the lowest and highest weights you could have, whilst maintaining a healthy BMI.)</li>
<li>BMI (Body Mass Index), adjusted +10% or -10% depending on frame size</li>
<li>Weight needed to achieve a goal BMI, also adjusted for frame size</li>
<li>BMR (Basal Metabolic Rate)</li>
<li>Total Daily Energy Expenditure (TDEE, which is your BMR plus your daily activity)</li>
<li>How many calories you need to eat per day, to lose a certain amount of weight per week. (0.5lbs, 1lb or 2lbs per week, and 0.25kg, 0.5kg and 1kg per week)</li>
</ul>

<h4>Weight loss predictors:</h4>
<ul>
<li>If you stick to your intended calorie intake per day, what date you will reach your goal weight</li>
<li>How much you will weigh on your goal finish date if you stick to your intended calorie intake per day</li>
<li>How many calories you’d need to consume per day, to achieve your goal weight on your goal finish date.</li>
</ul>

 
<h4>Diet Planners:</h4>
<ul>
<li>A zig-zag diet planner, to calculate how much you should eat each day, on a 7 day rotation, to achieve a varied calorie intake whilst maintaining an average of your specified number of calories. (Based on your intended calorie intake per day)</li>
<li>A 5:2 Diet planner (also known as the Fast Diet) to calculate your daily calorie intake based on your TDEE (Total Daily Energy Expenditure)</li>
<li>A macro calories and grams calculator - to show how many calories and grams of carbohydrate, protein and fat you should be consuming, whether you want to lose fat, maintain, or build muscle.</li>
</ul>
